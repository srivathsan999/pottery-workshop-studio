function updateBlogDetails() {
    const urlParams = new URLSearchParams(window.location.search);
    const id = urlParams.get('id');

    // Default to first post if no ID provided
    const post = blogData[id] || blogData['wheel-throwing-basics'];

    // Update Page Title
    document.title = `${post.title} - Earth & Clay Studio`;

    // Update Meta Description
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.content = post.shortDesc;

    const elements = {
        'post-date': (el) => el.textContent = post.date,
        'post-author': (el) => el.textContent = `By ${post.author}`,
        'post-title': (el) => el.textContent = post.title,
        'post-short-desc': (el) => el.textContent = post.shortDesc,
        'post-image': (el) => {
            el.src = post.image;
            el.alt = post.title;
        },
        'post-content': (el) => el.innerHTML = post.content
    };

    for (const [id, updateFn] of Object.entries(elements)) {
        const el = document.getElementById(id);
        if (el) updateFn(el);
    }
}

document.addEventListener('DOMContentLoaded', updateBlogDetails);
